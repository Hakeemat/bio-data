let operator = prompt("Enter operator:+,-,*,/")
let num1 = parseInt (prompt('Num1: '))
let num2 = parseInt (prompt('Num2: '))

if (operator == '+'){
    alert(num1 + num2);
 }
 else if (operator == '-')
        {alert(num1 - num2);
 }
 else if (operator == '*')
        {alert(num1 * num2);
 }
 else if (operator == '/')
        {alert(num1 / num2);
 }

